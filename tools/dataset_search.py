import json
from langchain.tools import tool
from pydantic import BaseModel, Field

class DatasetSearchInput(BaseModel):
    query: str = Field(..., description="Dataset search query")

@tool(args_schema=DatasetSearchInput)
def search_datasets(query: str) -> str:
    """Search for relevant datasets on Kaggle, HuggingFace, and GitHub"""
    formatted_query = query.replace(' ', '+')
    return json.dumps({
        "platforms": {
            "Kaggle": f"https://www.kaggle.com/search?q={formatted_query}",
            "HuggingFace": f"https://huggingface.co/datasets?search={formatted_query}",
            "GitHub": f"https://github.com/search?q={formatted_query}+dataset"
        },
        "note": "These are direct links to search results. For production use, implement API calls to these platforms."
    }, indent=2)

